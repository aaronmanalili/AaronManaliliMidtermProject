    <h2>The Yurts at Pacific Trails</h2>

    <h4>What is a yurt?</h4>
    <p>Our luxury yurts are permanent structures four feet off the ground. Each yurt has canvas walls, a wooden floor, and a roof dome that can be opened.</p>

    <h4>How are the yurts furnished?</h4>
    <p>Each yurt is finished with a queen-sized bed with down quilt and gas-fired stove.
        The luxury camping experience also includes electricity and a sink with hot and cold running water.
        Shower and restroom facilities are located in the lodge.</p>

    <h4>What should I bring?</h4>
    <p>Bring a sense of adventure and some time to relax! 
        Most guests also pack comfortable walking shoes and plan to dress for changing weather with layers of clothing.</p>

